def _expand_batches(tongzhao_data: dict) -> dict:
    """从统招数据自动生成名额到区/名额到校批次。

    规则（基于上海中考名额分配政策近似值）:
    - 市实验性示范性高中(city_key): 名额到区(-6~8分), 名额到校(-12~16分), 自主招生(quota 10-15)
    - 区实验性示范性高中(district_key): 名额到区(-3~5分), 无名额到校
    - 普通高中(regular): 仅统招
    """
    result = {}
    for sid, rows in tongzhao_data.items():
        tier = _TIER_MAP.get(sid, "regular")
        expanded = list(rows)

        for year, batch, score, quota, ctrl in rows:
            if batch != "统招":
                continue

            if tier == "city_key":
                expanded.append((year, "名额到区", round(score - 7, 1), int(quota * 1.5), ctrl))
                expanded.append((year, "名额到校", round(score - 14, 1), int(quota * 0.7), ctrl))
                expanded.append((year, "自主招生", round(score - 3.5, 1), 12, ctrl))
            elif tier == "district_key":
                expanded.append((year, "名额到区", round(score - 4.5, 1), int(quota * 1.2), ctrl))

        result[sid] = expanded
    return result


